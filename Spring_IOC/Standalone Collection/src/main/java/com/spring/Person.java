package com.spring;

import java.util.List;

public class Person
{
private int id;
private String name;
private List<Long> phno;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public List<Long> getPhno() {
	return phno;
}
public void setPhno(List<Long> phno) {
	this.phno = phno;
}

}
